# üìå –°–∫–ª–∞–¥—ã–≤–∞–µ–º –∏ –≤—ã—á–∏—Ç–∞–µ–º –ø–µ—Ä–∏–º–µ—Ç—Ä—ã, –∞ –Ω–µ –¥–ª–∏–Ω—É –∏ —à–∏—Ä–∏–Ω—É.
# üìå –ü—Ä–∏ –≤—ã—á–∏—Ç–∞–Ω–∏–∏ –Ω–µ –¥–æ–ø—É—Å–∫–∞–π—Ç–µ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π.

class Square:
    """–ö–ª–∞—Å—Å Square - –æ–ø–∏—Å—ã–≤–∞–µ—Ç –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫. –ü–æ–∑–≤–æ—è–ª–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å –æ–ø–µ—Ä–∞—Ü–∏–∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
    –Ω–∞–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞–º–∏ –∫–ª–∞—Å—Å–∞ Square. """
    def __init__(self, a, b=None):
        self.a = a
        if b:
            self.b = b
        else:
            self.b = a

    def area(self):
        """–ü–ª–æ—â–∞–¥—å –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞"""
        return self.a * self.b

    def perimetr(self):
        """–ü–µ—Ä–∏–º–µ—Ç—Ä –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞"""
        return 2 * (self.a + self.b)

    def __str__(self):
        """–ü–µ—á–∞—Ç—å –¥–∞–Ω–Ω—ã—Ö –æ–±—ä–µ–∫—Ç–∞ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è - –¥–ª—è —Ñ—É–Ω–∫—Ü–∏–π print, str"""
        return f'–î–∞–Ω–Ω—ã–µ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞: –î–ª–∏–Ω–∞={self.a}, —à–∏—Ä–∏–Ω–∞={self.b}, ' \
               f'–ø–ª–æ—â–∞–¥—å={self.area()}, –ø–µ—Ä–∏–º–µ—Ç—Ä={self.perimetr()}.'

    def __repr__(self):
        """–ü–µ—á–∞—Ç—å –¥–∞–Ω–Ω—ã—Ö –æ–±—ä–µ–∫—Ç–∞ –¥–ª—è –ø—Ä–æ–≥—Ä–∞–º–º–∏—Å—Ç–∞ - –≤ —Ä–µ–∂–∏–º–µ –æ—Ç–ª–∞–¥–∫–∏"""
        return f'=Square: {self.a=}, {self.b=}, {self.area()=}, {self.perimetr()=}'

    def __add__(self, other):
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å–ª–æ–∂–µ–Ω–∏—è –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        new_a = self.a + other.a
        new_b = self.b + other.b
        return Square(new_a, new_b)

    def __sub__(self, other):
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ –≤—ã—á–∏—Ç–∞–Ω–∏—è –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        if self.a - other.a < 0 or self.b - other.b < 0:
            raise ValueError
        new_a = self.a - other.a
        new_b = self.b - other.b
        return Square(new_a, new_b)

    def __eq__(self, other):
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è –Ω–∞ —Ä–∞–≤–µ–Ω—Å—Ç–≤–æ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() == other.area()

    def __ne__(self, other):
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è –Ω–∞ –Ω–µ—Ä–∞–≤–µ–Ω—Å—Ç–≤–æ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() != other.area()

    def __gt__(self, other):    # –±–æ–ª—å—à–µ
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è - "–±–æ–ª—å—à–µ" –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() > other.area()

    def __ge__(self, other):    # –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è - "–±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ" –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() >= other.area()

    def __lt__(self, other):    # –º–µ–Ω—å—à–µ
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è - "–º–µ–Ω—å—à–µ" –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() < other.area()

    def __le__(self, other):    # –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ
        """–¥–∞–Ω–¥–µ—Ä-–º–µ—Ç–æ–¥ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è - "–º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ" –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤"""
        return self.area() <= other.area()


