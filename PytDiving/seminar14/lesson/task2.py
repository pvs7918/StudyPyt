import doctest
from string import ascii_lowercase





def removal_chars(text: str) -> str:
    """
    >>> removal_chars("QWERTY") == "qwerty"
    True
    >>> removal_chars("QWER TY2") == "qwer ty"
    True
    """
    result = ''
    for ch in text.lower():
        if ch in ascii_lowercase + ' ':
            result += ch

    return result

